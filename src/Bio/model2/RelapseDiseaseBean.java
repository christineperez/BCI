package Bio.model2;

public class RelapseDiseaseBean {
	private int diseaseStatusId;
	private int relapseDiseaseId;
	private byte relapse;

	public int getDiseaseStatusId() {
		return diseaseStatusId;
	}

	public void setDiseaseStatusId(int diseaseStatusId) {
		this.diseaseStatusId = diseaseStatusId;
	}

	public int getRelapseDiseaseId() {
		return relapseDiseaseId;
	}

	public void setRelapseDiseaseId(int relapseDiseaseId) {
		this.relapseDiseaseId = relapseDiseaseId;
	}

	public byte getRelapse() {
		return relapse;
	}

	public void setRelapse(byte relapse) {
		this.relapse = relapse;
	}

}
