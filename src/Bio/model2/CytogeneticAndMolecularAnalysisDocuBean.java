package Bio.model2;

public class CytogeneticAndMolecularAnalysisDocuBean {
	private int labProfIdNumber;
	private int cytoAndMolecularId;
	private String status;
	private String result;
	private String type;

	public int getLabProfIdNumber() {
		return labProfIdNumber;
	}

	public void setLabProfIdNumber(int labProfIdNumber) {
		this.labProfIdNumber = labProfIdNumber;
	}

	public int getCytoAndMolecularId() {
		return cytoAndMolecularId;
	}

	public void setCytoAndMolecularId(int cytoAndMolecularId) {
		this.cytoAndMolecularId = cytoAndMolecularId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
