package Bio.model2;

public class SmokingHistoryBean {
private int clinicalDataIdNumber;
private int smokingHistoryId;
private String specification;

public int getClinicalDataIdNumber() {
	return clinicalDataIdNumber;
}
public void setClinicalDataIdNumber(int clinicalDataIdNumber) {
	this.clinicalDataIdNumber = clinicalDataIdNumber;
}
public int getSmokingHistoryId() {
	return smokingHistoryId;
}
public void setSmokingHistoryId(int smokingHistoryId) {
	this.smokingHistoryId = smokingHistoryId;
}
public String getSpecification() {
	return specification;
}
public void setSpecification(String specification) {
	this.specification = specification;
}
}
