package Bio.model2;

public class SerumBean {
	private int labProfIdNumber;
	private int serumId;
	private String status;
	private String result;

	public int getLabProfIdNumber() {
		return labProfIdNumber;
	}

	public void setLabProfIdNumber(int labProfIdNumber) {
		this.labProfIdNumber = labProfIdNumber;
	}

	public int getSerumId() {
		return serumId;
	}

	public void setSerumId(int serumId) {
		this.serumId = serumId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

}
