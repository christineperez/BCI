package Bio.model;

public class OtherRegimenBean {
	private int otherRegId;
	private int regimenTransplantId;
	private int regimenNonTransplantId;
	private int maintenanceId;
	private String otherRegimen;

	public int getOtherRegId() {
		return otherRegId;
	}

	public void setOtherRegId(int otherRegId) {
		this.otherRegId = otherRegId;
	}

	public int getRegimenTransplantId() {
		return regimenTransplantId;
	}

	public void setRegimenTransplantId(int regimenTransplantId) {
		this.regimenTransplantId = regimenTransplantId;
	}

	public int getRegimenNonTransplantId() {
		return regimenNonTransplantId;
	}

	public void setRegimenNonTransplantId(int regimenNonTransplantId) {
		this.regimenNonTransplantId = regimenNonTransplantId;
	}

	public int getMaintenanceId() {
		return maintenanceId;
	}

	public void setMaintenanceId(int maintenanceId) {
		this.maintenanceId = maintenanceId;
	}

	public String getOtherRegimen() {
		return otherRegimen;
	}

	public void setOtherRegimen(String otherRegimen) {
		this.otherRegimen = otherRegimen;
	}

}
