package Bio.model;

public class OtherLaboratoriesBean {
private int clinicalDataIdNumber;
private int otherLaboratoriesId;
private String parameter;
private String result;
private String notDone;

public int getClinicalDataIdNumber() {
	return clinicalDataIdNumber;
}
public void setClinicalDataIdNumber(int clinicalDataIdNumber) {
	this.clinicalDataIdNumber = clinicalDataIdNumber;
}
public int getOtherLaboratoriesId() {
	return otherLaboratoriesId;
}
public void setOtherLaboratoriesId(int otherLaboratoriesId) {
	this.otherLaboratoriesId = otherLaboratoriesId;
}
public String getParameter() {
	return parameter;
}
public void setParameter(String parameter) {
	this.parameter = parameter;
}
public String getResult() {
	return result;
}
public void setResult(String result) {
	this.result = result;
}
public String getNotDone() {
	return notDone;
}
public void setNotDone(String notDone) {
	this.notDone = notDone;
}
}
