package Bio.model;

public class ChangesInPeBean {
	private int changesInPeId;
	private int followUpIdNumber;
	private String specification;

	public int getChangesInPeId() {
		return changesInPeId;
	}

	public void setChangesInPeId(int changesInPeId) {
		this.changesInPeId = changesInPeId;
	}

	public int getFollowUpIdNumber() {
		return followUpIdNumber;
	}

	public void setFollowUpIdNumber(int followUpIdNumber) {
		this.followUpIdNumber = followUpIdNumber;
	}

	public String getSpecification() {
		return specification;
	}

	public void setSpecification(String specification) {
		this.specification = specification;
	}

}
