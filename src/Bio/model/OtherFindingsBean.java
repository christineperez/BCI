package Bio.model;

public class OtherFindingsBean {
private int clinicalDataIdNumber;
private int otherFindingsId;
private String findings;
public int getClinicalDataIdNumber() {
	return clinicalDataIdNumber;
}
public void setClinicalDataIdNumber(int clinicalDataIdNumber) {
	this.clinicalDataIdNumber = clinicalDataIdNumber;
}
public int getOtherFindingsId() {
	return otherFindingsId;
}
public void setOtherFindingsId(int otherFindingsId) {
	this.otherFindingsId = otherFindingsId;
}
public String getFindings() {
	return findings;
}
public void setFindings(String findings) {
	this.findings = findings;
}

}
