package Bio.model;

public class DiseaseStatusTreatmentBean {
private int diseaseStatusIdNumber;
private int treatmentIdNumber;
private String modeOfTreatment;

public int getDiseaseStatusIdNumber() {
	return diseaseStatusIdNumber;
}
public void setDiseaseStatusIdNumber(int diseaseStatusIdNumber) {
	this.diseaseStatusIdNumber = diseaseStatusIdNumber;
}
public int getTreatmentIdNumber() {
	return treatmentIdNumber;
}
public void setTreatmentIdNumber(int treatmentIdNumber) {
	this.treatmentIdNumber = treatmentIdNumber;
}
public String getModeOfTreatment() {
	return modeOfTreatment;
}
public void setModeOfTreatment(String modeOfTreatment) {
	this.modeOfTreatment = modeOfTreatment;
}

}
