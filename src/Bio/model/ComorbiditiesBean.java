package Bio.model;

public class ComorbiditiesBean {
private int clinicalDataIdNumber;
private int comorbiditiesNumber;
private String comorbidities;

public int getClinicalDataIdNumber() {
	return clinicalDataIdNumber;
}
public void setClinicalDataIdNumber(int clinicalDataIdNumber) {
	this.clinicalDataIdNumber = clinicalDataIdNumber;
}
public int getComorbiditiesNumber() {
	return comorbiditiesNumber;
}
public void setComorbiditiesNumber(int comorbiditiesNumber) {
	this.comorbiditiesNumber = comorbiditiesNumber;
}
public String getComorbidities() {
	return comorbidities;
}
public void setComorbidities(String comorbidities) {
	this.comorbidities = comorbidities;
}
}
