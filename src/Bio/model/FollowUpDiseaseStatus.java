package Bio.model;

public class FollowUpDiseaseStatus {
private int followUpDiseaseStatusId;
private String diseaseStatus;
private String others;
private String notes;
public int getFollowUpDiseaseStatusId() {
	return followUpDiseaseStatusId;
}
public void setFollowUpDiseaseStatusId(int followUpDiseaseStatusId) {
	this.followUpDiseaseStatusId = followUpDiseaseStatusId;
}
public String getDiseaseStatus() {
	return diseaseStatus;
}
public void setDiseaseStatus(String diseaseStatus) {
	this.diseaseStatus = diseaseStatus;
}
public String getOthers() {
	return others;
}
public void setOthers(String others) {
	this.others = others;
}
public String getNotes() {
	return notes;
}
public void setNotes(String notes) {
	this.notes = notes;
}


}
